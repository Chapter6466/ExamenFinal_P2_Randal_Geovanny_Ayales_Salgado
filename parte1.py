#1.	Liste 2 diferencias entre listas y diccionarios en Python.

    # 1. Para entender la diferencia entre listas y diccionarios en Python, piensa en esto: las listas son como una fila ordenada de elementos donde cada uno tiene su número de posición. Por ejemplo, cuando guardas una lista de compras, cada artículo está en un orden específico. Los diccionarios, en cambio, son más como un directorio telefónico - cada valor está asociado con una palabra clave única, sin importar el orden. No puedes tener dos contactos con el mismo nombre (clave), pero en una lista de compras sí podrías anotar "manzanas" dos veces.

#2.	Que es la herencia.

    # 2. La herencia en programación es similar a la herencia biológica: imagina que tienes una clase "Padre" con ciertas características y funciones. Cuando creas una clase "Hija", esta automáticamente obtiene todas esas características del padre, y además puedes agregarle sus propias características únicas. Es una forma muy práctica de reutilizar código sin tener que escribirlo todo desde cero.

#3.	Mencione 2 casos donde la herencia puede ayudarnos a mejorar nuestro código.

    # 3. La herencia es especialmente útil en situaciones de programación. Por ejemplo, si estás programando un sistema para una concesionaria de vehículos, podrías tener una clase base "Vehículo" que contenga todas las características comunes como motor, ruedas y velocidad máxima. Luego, cuando necesites crear clases específicas como "Auto", "Moto" o "Camión", no tendrás que volver a escribir todas esas características básicas - simplemente las heredan de "Vehículo" y solo agregas lo que las hace únicas.
